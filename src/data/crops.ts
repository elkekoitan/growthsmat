export type EvidenceGrade = "A" | "B" | "C" | "D" | "E";

export type GrowMethod =
  | "acik-alan"
  | "yukseltilmis-yatak"
  | "saksi"
  | "sera"
  | "hidroponik";

export type Season = "ilkbahar" | "yaz" | "sonbahar" | "kis";

export type CropCategory =
  | "meyveli-sebze"
  | "yaprakli"
  | "aromatik"
  | "kok-yumru"
  | "baklagil"
  | "meyve"
  | "agac-meyve"; // yeni: çok yıllık ağaç meyve/kuruyemiş (zeytin, turunçgil vb.) — 2026-07-12

export interface CompanionRelation {
  cropId: string;
  effect: "faydali" | "notr" | "riskli";
  mechanism: string; // alan | kök | zaman | azot | zararlı | polinatör | rekabet
  evidence: EvidenceGrade;
}

export interface Crop {
  id: string;
  name: string;
  cultivarNote: string; // çeşit/pazar tipi profili — tür geneli değil
  scientificName: string;
  family: string;
  category: CropCategory;
  emoji: string;
  description: string;
  methods: GrowMethod[];
  sunMinHours: number;
  sunOptHours: number;
  tempC: { min: number; optLow: number; optHigh: number; max: number };
  ph: { min: number; optLow: number; optHigh: number; max: number };
  waterNeed: 1 | 2 | 3; // 1 düşük · 2 orta · 3 yüksek
  careLoad: 1 | 2 | 3; // haftalık bakım yükü
  minContainerL: number | null; // saksı yöntemi için minimum kök hacmi
  spacingCm: number;
  daysToHarvest: [number, number];
  yieldPerM2Kg: [number, number];
  frostTolerant: boolean;
  petCaution: boolean; // evcil hayvan/çocuk temkin notu
  beginnerFriendly: boolean;
  plantingSeasons: Season[];
  successionCrop: boolean; // ardışık ekime uygun
  companions: CompanionRelation[];
  rotationAvoidFamilies: string[];
  marketDemand: 1 | 2 | 3;
  pestResilience: 1 | 2 | 3;
  sustainability: 1 | 2 | 3;
  evidence: EvidenceGrade;
  source: { title: string; org: string; year: number; url?: string };
  tips: string[];
}

